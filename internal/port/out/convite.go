package out

import (
	"context"
	"time"

	convitedomain "github.com/role-organizado/backend-go-role-organizado/internal/domain/convite"
)

// ============================================================
// Convites domain — repository ports (output / driven side).
// These ports are convite-specific because the domain types
// carry richer semantics (TipoParticipante, Papel, status enum)
// than the finance-domain Participant.
// ============================================================

// ConviteParticipantRepository persists participant documents from the convite
// domain perspective.
type ConviteParticipantRepository interface {
	FindByID(ctx context.Context, id string) (*convitedomain.Participant, error)
	FindByEventoIDAndUsuarioID(ctx context.Context, eventoID, usuarioID string) (*convitedomain.Participant, error)
	FindByEventoIDAndPapel(ctx context.Context, eventoID string, papel convitedomain.Papel) ([]convitedomain.Participant, error)
	Save(ctx context.Context, p *convitedomain.Participant) (*convitedomain.Participant, error)
	UpdateStatus(ctx context.Context, participantID string, status convitedomain.ParticipantStatus, dataResposta *time.Time) error
	BindUsuarioID(ctx context.Context, participantID, usuarioID string) error
}

// ConviteGuestRepository persists guest documents.
type ConviteGuestRepository interface {
	FindByID(ctx context.Context, id string) (*convitedomain.Guest, error)
	FindByTelefoneOrEmail(ctx context.Context, telefone, email string) (*convitedomain.Guest, error)
	Save(ctx context.Context, g *convitedomain.Guest) (*convitedomain.Guest, error)
}

// ConviteApprovalRepository persists ApprovalItem documents of type INVITE.
type ConviteApprovalRepository interface {
	FindByID(ctx context.Context, id string) (*convitedomain.ApprovalItem, error)
	FindLatestByTargetEntityIDAndType(ctx context.Context, targetEntityID string, t convitedomain.ApprovalItemType) (*convitedomain.ApprovalItem, error)
	FindPendingByTargetEntityID(ctx context.Context, targetEntityID string) (*convitedomain.ApprovalItem, error)
	ExistsPendingByTargetEntityID(ctx context.Context, targetEntityID string) (bool, error)
	Save(ctx context.Context, a *convitedomain.ApprovalItem) (*convitedomain.ApprovalItem, error)
	UpdateStatus(ctx context.Context, id string, status convitedomain.ApprovalItemStatus, resolvedBy, note string, resolvedAt time.Time) error
}

// ParticipantCreditRepository persists refund credits generated by desistência.
type ParticipantCreditRepository interface {
	Save(ctx context.Context, c ParticipantCredit) (string, error)
}

// ParticipantCredit is a value-object passed across the port boundary.
type ParticipantCredit struct {
	ID             string
	ParticipantID  string
	EventoID       string
	UsuarioID      string
	AmountCents    int64
	Reason         string
	Status         string
	CreatedAt      time.Time
}

// ConvitePoliticaCancelamentoRepository loads cancellation tiers from the dominios
// collection (categoria=politica_cancelamento).
type ConvitePoliticaCancelamentoRepository interface {
	FindByChave(ctx context.Context, chave string) (*convitedomain.CancellationPolicy, error)
}

// ConviteInstallmentRepository wraps the payment_installments operations needed
// by the desistência use case. Kept narrow on purpose to honour ISP.
type ConviteInstallmentRepository interface {
	FindByParticipantID(ctx context.Context, eventoID, participantID string) ([]ConviteInstallment, error)
	CancelPendingByParticipantID(ctx context.Context, eventoID, participantID string) (int64, error)
}

// ConviteInstallment is a narrow projection of a payment installment.
type ConviteInstallment struct {
	ID         string
	Status     string
	AmountCents int64
	PaidAt     *time.Time
}

// ConviteAuditRepository persists audit_entries records for admin actions.
type ConviteAuditRepository interface {
	Save(ctx context.Context, e ConviteAuditEntry) error
}

// ConviteAuditEntry is the persistence-side value-object.
type ConviteAuditEntry struct {
	Acao       string
	EventoID   string
	AdminID    string
	Detalhes   map[string]any
	OcorridoEm time.Time
}

// ConviteNotificationPort enqueues an outbound invite delivery (SQS in prod).
type ConviteNotificationPort interface {
	PublicarConvite(ctx context.Context, in ConvitePublishInput) (messageID string, err error)
}

// ConvitePublishInput carries all data required for an SQS payload.
type ConvitePublishInput struct {
	ParticipantID   string
	Canal           string
	Telefone        string
	Email           string
	Nome            string
	EventoID        string
	EventoNome      string
	EventoData      *time.Time
	EventoLocal     string
	OrganizadorNome string
}
